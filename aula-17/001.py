# TUPLAS:
tupla1 = 10, 'joão', 3.2, True

# for - tupla
for i in tupla1:
    print(i)

print('------')
# while - - tupla
# print(f'Tamanho tupla: {len(tupla1)}')
i = 0
while i < len(tupla1):
    print(tupla1[i])
    i += 1

print('------')

